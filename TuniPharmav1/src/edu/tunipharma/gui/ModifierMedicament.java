    public ModifierMedicament(int id) {
        initComponents();
        
       Medicament m= new Medicament();
        int idMedicament=id;
       m= mdcdao.findMedicamentById(id);
       tbIdMdc.enable(false);
       tbIdMdc.setText(Integer.toString(m.getId_mdc()));
       tbRef.setText(m.getRef());
       tbLib.setText(m.getLibelle());
       tbPrix.setText(Float.toString(m.getPrix()));
       tbIdPh.setText(Integer.toString(m.getId_ph()));
       
       if(m.getType().equalsIgnoreCase("Certifier"))
       {
           cbType.setSelectedIndex(0);
       }else
       {
           cbType.setSelectedIndex(1);
       }
    }

